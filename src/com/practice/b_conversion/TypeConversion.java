package com.practice.b_conversion;

public class TypeConversion {
    public static void main(String[] args) {
        /*
        Conversion of Datatype automatically is called type conversion.

        It can be only done from:

        Byte -> Short -> Int -> Long -> Float -> Double

         */

        int a = 10;

        float b = a;

        System.out.println("I've assigned a to int but I got decimal value which is: "+b);

    }
}
